    private void btnValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderMouseClicked
        // Action apres click  du noutton valider

        if (txtNom.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Le Nom ne peux pas etre vie !", "System Info", JOptionPane.WARNING_MESSAGE);
            txtNom.requestFocus();

        } else if (txtPrenom.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Le Prenom  ne peux pas etre vie !", "System Info", JOptionPane.WARNING_MESSAGE);
            txtPrenom.requestFocus();

        } else if (txtNumPiece.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Le Numero de piece ne peux pas etre vie !", "System Info", JOptionPane.WARNING_MESSAGE);
            txtNumPiece.requestFocus();

        } else if (txtPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Le numero de telephone  ne peux pas etre vie !", "System Info", JOptionPane.WARNING_MESSAGE);
            txtPhone.requestFocus();

        } else if (txtNameUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Le Num d'utilisateur (login)  ne peux pas etre vie !", "System Info", JOptionPane.WARNING_MESSAGE);
            txtNameUser.requestFocus();

        } else if (txtpassUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Le mot de passe ne peux pas etre vie !", "System Info", JOptionPane.WARNING_MESSAGE);
            txtpassUser.requestFocus();

        } else {
            try {
                System.out.println("Resultat de la fonction recherche = " + user.rechercheUser(numero));
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FormUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                if (user.rechercheUser(numero) == false) {
                    // recuperation des valeurs
                    String nom = txtNom.getText();
                    String prenom = txtPrenom.getText();
                    String typePiece = (String) cmbTypePiece.getSelectedItem();
                    String numPiece = txtNumPiece.getText();
                    String phone = txtPhone.getText();
                    String role = (String) cmbRole.getSelectedItem();
                    String userName = txtNameUser.getText();
                    String UserPasse = txtpassUser.getText();
//                    enregistrement des donnees
                    user.validerUser(numero, nom, prenom, phone, typePiece, numPiece, userName, UserPasse, role);
                    JOptionPane.showMessageDialog(null, "Donnees de l'utilisateur [ " + userName + " ] Enregistrer !");

                    allClear();
                    allDisable();
                    affichageList();
                    btnValider.setText("Valider");

                } else if (user.rechercheUser(numero) == true) {

                    // recuperation des valeurs
                    String nom = txtNom.getText();
                    String prenom = txtPrenom.getText();
                    String typePiece = (String) cmbTypePiece.getSelectedItem();
                    String numPiece = txtNumPiece.getText();
                    String phone = txtPhone.getText();
                    String role = (String) cmbRole.getSelectedItem();
                    String userName = txtNameUser.getText();
                    String UserPasse = txtpassUser.getText();

                    //Update des donnees d'un user
                    user.UpdateUser(getNumMdif.getText(), nom, prenom, phone, typePiece, numPiece, userName, UserPasse, role);
                    JOptionPane.showMessageDialog(null, "Donnees de l'utilisateur [ " + userName + " ] Modifier !");
                    allClear();
                    allDisable();
                    affichageList();
                    btnValider.setText("Valider");

                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FormUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
            }
            allDisable();
            affichageList();
            allClear();
            btnSupprimer.enableInputMethods(false);
        }
    }//GEN-LAST:event_btnValiderMouseClicked

    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) tableUser.getModel();
        int tIndex = tableUser.getSelectedRow();
        getNumMdif.setText(dtm.getValueAt(tIndex, 0).toString());
        numero = dtm.getValueAt(tIndex, 0).toString();

        try {

            if (null != user.getOneUser(getNumMdif.getText())) {
                String[] result = user.getOneUser(getNumMdif.getText());
                txtNom.setText(result[0]);
                txtPrenom.setText(result[1]);
                if (result[2].equals("CNIB")) {
                    cmbTypePiece.setSelectedIndex(0);
                } else {
                    cmbTypePiece.setSelectedIndex(1);
                }
                txtNumPiece.setText(result[3]);
                txtPhone.setText(result[4]);
                if (result[5].equals("ADMINISTRATEUR")) {
                    cmbRole.setSelectedIndex(1);
                } else {
                    cmbRole.setSelectedIndex(0);
                }
                txtNameUser.setText(result[6]);
                txtpassUser.setText(result[7]);
            }

            allEnable();
            btnSupprimer.enableInputMethods(true);
            btnValider.setText("Modifier");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(FormUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_tableUserMouseClicked

    private void btnSupprimerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimerMouseClicked
        try {
            if (JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment Supprimer l'utilisateur [ " + txtNameUser.getText() + " ]", "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                user.DeleteUsers(getNumMdif.getText());
                allClear();
                allDisable();
                affichageList();
                btnValider.setText("Valider");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FormUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSupprimerMouseClicked

