    public void allEnable() { // fonction d'activation des champs
        txtNom.enable(true);
        txtPrenom.enable(true);
        cmbTypePiece.enable(true);
        txtNumPiece.enable(true);
        txtPhone.enable(true);
        cmbRole.enable(true);
        txtNameUser.enable(true);
        txtpassUser.enable(true);

        btnAnnuler.enable(true);
        btnValider.enable(true);
    }

    public void allDisable() { //fonction de desactivation des champs
        txtNom.enable(false);
        txtPrenom.enable(false);
        cmbTypePiece.enable(false);
        txtNumPiece.enable(false);
        txtPhone.enable(false);
        cmbRole.enable(false);
        txtNameUser.enable(false);
        txtpassUser.enable(false);

        btnAnnuler.enable(false);
        btnValider.enable(false);
    }

    public void allClear() { //fonction de clear des champs
        txtNom.setText(null);
        txtPrenom.setText(null);
        cmbTypePiece.setSelectedIndex(0);
        txtNumPiece.setText(null);
        txtPhone.setText(null);
        cmbRole.setSelectedIndex(0);
        txtNameUser.setText(null);
        txtpassUser.setText(null);
        btnValider.setText("Valider");

    }

    public void affichageList() {
        DefaultTableModel tm = (DefaultTableModel) tableUser.getModel();
        tm.setRowCount(0);
        try {
            user.getListUser(tm);
            if (tableUser.getRowCount() != 0) {
                btnSupprimer.setEnabled(false);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(formEnvoie.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
